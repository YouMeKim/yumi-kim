            <h1>Visualization</h1>
            <p>After many brainstorming and sketching sessions, we decided that our favorite solution was a circular visualization that was otherwise literal in it's translation. We used the note, octave, beat, and duration and wrapped the timeline around a circle so when complete, you can't tell where it starts or ends.</p>
            <div class="fourth-first">
                <h4>NOTE = POSITION</h4>
                <p>The note played on the beam is translated onto the visual as a position relative to the center of the circle.</p>
            </div>
            <div class="fourth">
                <h4>OCTAVE = COLOR</h4>
                <p>Since each beam is a color, the color of the note placed on the visual is representative of which beam was played.</p>
            </div>
            <div class="fourth">
                <h4>BEAT = PLACEMENT</h4>
                <p>Seeing a note appear on the visual is the indication of beat. They shoot out from the center to their position around the circle.</p>
            </div>
            <div class="fourth">
                <h4>DURATION = SIZE</h4>
                <p>Once the notes are placed, they continue to grow until the hand is removed and the note is no longer being played.</p>
